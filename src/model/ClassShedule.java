package model;

/**
 *
 * @author sanch
 */
public class ClassShedule {
    
    private String classId;
    private String teacherTno;
    private String teacherNic;
    private String teacherName;
    private String subjectName;
    private String date;
    private String startAt;
    private String endAt;
    private String status;
    
    public ClassShedule(){
    
    }

    public String getClassId() {
        return classId;
    }

    public void setClassId(String classId) {
        this.classId = classId;
    }

    public String getTeacherTno() {
        return teacherTno;
    }

    public void setTeacherTno(String teacherTno) {
        this.teacherTno = teacherTno;
    }

    public String getTeacherNic() {
        return teacherNic;
    }

    public void setTeacherNic(String teacherNic) {
        this.teacherNic = teacherNic;
    }

    public String getTeacherName() {
        return teacherName;
    }

    public void setTeacherName(String teacherName) {
        this.teacherName = teacherName;
    }

    public String getSubjectName() {
        return subjectName;
    }

    public void setSubjectName(String subjectName) {
        this.subjectName = subjectName;
    }

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }

    public String getStartAt() {
        return startAt;
    }

    public void setStartAt(String startAt) {
        this.startAt = startAt;
    }

    public String getEndAt() {
        return endAt;
    }

    public void setEndAt(String endAt) {
        this.endAt = endAt;
    }

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }

    

    
}
